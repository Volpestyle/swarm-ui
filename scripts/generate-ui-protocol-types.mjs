import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const UI_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SOURCE = process.env.SWARM_PROTOCOL_STATE_RS
  ? resolve(process.cwd(), process.env.SWARM_PROTOCOL_STATE_RS)
  : resolve(UI_ROOT, "../../crates/swarm-protocol/src/state.rs");
const OUTPUT = resolve(UI_ROOT, "src/lib/generated/protocol.ts");

const TARGET_ENUMS = ["InstanceStatus", "TaskStatus", "TaskType"];
const TARGET_STRUCTS = [
  "Instance",
  "Task",
  "Message",
  "Lock",
  "Annotation",
  "Event",
  "KvEntry",
];

const rust = readFileSync(SOURCE, "utf8");

function toSnakeCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1_$2")
    .toLowerCase();
}

function mapType(type) {
  const trimmed = type.trim();

  const option = trimmed.match(/^Option<(.+)>$/);
  if (option) return `${mapType(option[1])} | null`;

  const vec = trimmed.match(/^Vec<(.+)>$/);
  if (vec) return `${mapType(vec[1])}[]`;

  const primitiveMap = new Map([
    ["String", "string"],
    ["bool", "boolean"],
    ["i64", "number"],
    ["i32", "number"],
    ["u64", "number"],
    ["u32", "number"],
    ["u16", "number"],
    ["usize", "number"],
  ]);
  if (primitiveMap.has(trimmed)) return primitiveMap.get(trimmed);

  return trimmed.split("::").at(-1);
}

function extractBlock(kind, name) {
  const match = rust.match(
    new RegExp(`pub ${kind} ${name} \\{([\\s\\S]*?)\\n\\}`, "m"),
  );
  if (!match) {
    throw new Error(`Could not find ${kind} ${name} in ${SOURCE}`);
  }
  return match[1];
}

function parseEnum(name) {
  const body = extractBlock("enum", name);
  const values = body
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("#["))
    .map((line) => line.replace(/,$/, ""))
    .map((line) => `'${toSnakeCase(line)}'`);
  return `export type ${name} = ${values.join(" | ")};`;
}

function parseStruct(name) {
  const body = extractBlock("struct", name);
  const fields = [];
  let rename = null;

  for (const rawLine of body.split("\n")) {
    const line = rawLine.trim();
    if (!line) continue;

    const renameMatch = line.match(/^#\[serde\(rename = "([^"]+)"\)\]$/);
    if (renameMatch) {
      rename = renameMatch[1];
      continue;
    }

    if (line.startsWith("#[")) continue;

    const field = line.match(/^pub ([a-zA-Z0-9_]+): (.+),$/);
    if (!field) continue;

    const name = rename ?? field[1];
    fields.push(`  ${name}: ${mapType(field[2])};`);
    rename = null;
  }

  return [`export interface ${name} {`, ...fields, "}"].join("\n");
}

const output = `// This file is generated by scripts/generate-ui-protocol-types.mjs.
// Source of truth: crates/swarm-protocol/src/state.rs
// Do not edit by hand.

${TARGET_ENUMS.map(parseEnum).join("\n\n")}

${TARGET_STRUCTS.map(parseStruct).join("\n\n")}
`;

mkdirSync(dirname(OUTPUT), { recursive: true });
writeFileSync(OUTPUT, output);
console.log(`wrote ${OUTPUT}`);
